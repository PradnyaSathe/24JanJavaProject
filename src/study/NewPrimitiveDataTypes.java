package study;

public class NewPrimitiveDataTypes {
    static void main(String[] args) {
        // I want to store all types of primitive data types
        // there are 8 types-byte,short,int,long,float,double,char,boolean
        //we need to add f or d after assigning the value of float or double

        // first I declare the variables
        byte overs;
        short runs;
        int salary;
        long numbers;
        float marks;
        double goldWeight;
        char division;
        boolean lightStatus; // on-->true  off-->false

        // Now I am assigning the values
        overs=15;
        runs=10000;
        salary=200000;
        numbers=1345678909;
        marks=98.234f;
        goldWeight=8.123456788d;
        division='B';
        lightStatus=false;
        //now I want to print all values
        System.out.println("my overs is "+overs);
        System.out.println("my runs are "+runs);
        System.out.println("my salary is "+salary);
        System.out.println("my numbers are "+numbers);
        System.out.println("my marks are "+marks);
        System.out.println("gold weight is "+goldWeight);
        System.out.println("my division is "+division);

    }

    }

