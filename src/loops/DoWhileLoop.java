package loops;

public class DoWhileLoop {
    static void main(String[] args) {
        int a=3;
        do
            {
                System.out.println(a);
                a=a+3;
            }while(a<=30);

        //print name
        int b=0;
        int sum=7;
        do{
            sum=sum+b;
            System.out.println(sum);
            b++;
        }while(b<=3);
        }
    }

