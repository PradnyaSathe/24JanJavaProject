package loops;

public class ForLoop1 {
    static void main(String[] args) {
        //i want to write a to z letters
        for(char a='A' ; a<='Z' ; a++)
        {
            System.out.println(a);
        }

        for(float b=20.60f ; b<=40.60f ; b=b+2 )
        {
            System.out.println(b);
        }
    }
}
