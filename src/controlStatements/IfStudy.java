package controlStatements;

public class IfStudy {
    static void main(String[] args) {
        int a=20;
        int b=30;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);

        int c=40;
        int d=30;
        //greater than or equal to codition

        System.out.println(c>=d);
        System.out.println(c<=d);
        System.out.println(c==d);
        System.out.println(c!=d);
    }
}
