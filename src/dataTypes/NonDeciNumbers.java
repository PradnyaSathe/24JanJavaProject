package dataTypes;

public class NonDeciNumbers {
    static void main() {
        // I want to store my data(my marks)
        byte marks;  // variable declaration
        marks= 100;  // variable initialization
        System.out.println("my marks is "+marks);

        // I want to store my salary
        short salary;
        salary=30000;
        System.out.println("my salary is "+salary);

        // I want to store my pinCode
        int pinCode;
        pinCode=415124;
        System.out.println("my pincode is "+pinCode);

        // I want to store data
        long data;
        data=123456789;
        System.out.println("my data is "+data);

    }
}
