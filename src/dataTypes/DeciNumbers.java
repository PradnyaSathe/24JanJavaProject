package dataTypes;

public class DeciNumbers {
    static void main() {
        // I want store decimal number
        float weight;  // variable declaration
        weight=55.6789f; // variable initialization
        System.out.println("my weight is "+weight);

        // I want to store my height
        float height;
        height=6;
        System.out.println("my height is"+height);

        // I want to store goldWeight
        double goldWeight;
        goldWeight=56456.1234567890d;
        System.out.println("my gold weight is "+goldWeight);
    }
}
