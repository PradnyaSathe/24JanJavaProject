package dataTypes;

public class DecimalNumbers {
    static void main() {
        // I want to write cricket overs
        float overs;
        overs=10.6f;
        System.out.println("cricket over is "+overs);

        // I want to store my height
        float height; // variable declaration
     height=5.5f;  // variable assignment
      System.out.println("my height is "+height);

      // I want to store gold_Weight
        double gold_Weight;  // variable declaration
        gold_Weight=6.2345678291d;  // variable assignment
        System.out.println("gold weight is "+gold_Weight);

      // I want store my percentage
      float percentage;  // variable declaration
      percentage=95.45f;  // variable assignment
      System.out.println("my percentage is "+percentage);



    }
}
