package dataTypes;

public class NewBooleanTypes {
    static void main(String[] args) {
        // I want to store attendence
        // present-->true  absent-->false
        boolean attendence;  // variable declaration
        attendence=true;  // variable initialization
        System.out.println("my attendence is "+attendence);
        // I want to store light status
        // yes-->true no-->false
        boolean lightStatus;
        lightStatus=false;
        System.out.println("Light status is "+lightStatus);
    }
}
